# practice with string
s = 'Sarvesh123'
print(s.isalnum())  # checks whether the string contains number or not
print(s.isalpha())  # checks whether the all string contains character or not
print(s.isdigit())  # checks whether the all Digit contains character or not
print(s.istitle())  # checks whether the first alphabet is capital or not
print(s.isupper())  # checks whether the all the character in string is in uppercase
print(s.islower())  # checks whether the all the character in string is in lowercase
print(s.isspace())  # checks whether the string contains space or not
print(s.startswith('S'))  # checks whether the string starts with 'S'
print(s.endswith('S'))  # checks whether the string ends with 'S'
